function getPieceSquareValue(piece, row, col) {
  const table = PIECE_SQUARE_TABLES[piece.type];

  if (!table) return 0;

  const index = piece.color === "w" ? row * 8 + col : (7 - row) * 8 + col;

  return table[index];
}

export function evaluateBoard(game) {
  if (game.isCheckmate()) {
    return game.turn() === "w" ? -100000 : 100000;
  }

  if (game.isDraw()) {
    return 0;
  }

  let score = 0;
  const board = game.board();

  for (let row = 0; row < 8; row++) {
    for (let col = 0; col < 8; col++) {
      const piece = board[row][col];

      if (!piece) continue;

      const material = PIECE_VALUES[piece.type];
      const position = getPieceSquareValue(piece, row, col);
      const pieceScore = material + position;

      score += piece.color === "w" ? pieceScore : -pieceScore;
    }
  }

  if (game.isCheck()) {
    score += game.turn() === "w" ? -30 : 30;
  }

  return score;
}